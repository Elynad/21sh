#include "../includes/ft_sh2.h"

int			check_str(char *str)
{
	int		a;

	a = 0;
	while (str[a])
	{
		if (str[a] != ' ' && str[a] != '\t')
			return (0);
		a++;
	}
	return (-1);
}
